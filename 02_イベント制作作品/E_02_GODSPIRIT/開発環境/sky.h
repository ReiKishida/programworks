#ifndef _SKY_H_
#define _SKY_H_

#include "main.h"
//******************************************************************************************************************************
// 構造体定義
//******************************************************************************************************************************
typedef enum
{
	SKYTYPE_0 = 0,
	SKYTYPE_MAX
}SKYTYPE;

typedef struct
{
	D3DXVECTOR3 pos;	// 位置
	D3DXVECTOR3 rot;	// 向き
	D3DXMATRIX mtxWorld;// ワールドマトリックス
	float fWidth;		// 幅
	float fHeight;		// 高さ
	float fDepth;		// 奥行き
	float fLength;		// 半径
	SKYTYPE nType;	// 種類
	bool bUse;			// 使用されているかどうか
}Sky;

//******************************************************************************************************************************
// プロトタイプ宣言
//******************************************************************************************************************************
void InitSky(void);
void UninitSky(void);
void UpdateSky(void);
void DrawSky(void);
#endif
