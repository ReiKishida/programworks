#ifndef _UI_H_
#define _UI_H_

#include "main.h"
//******************************************************************************************************************************
// マクロ定義
//******************************************************************************************************************************

//******************************************************************************************************************************
// 構造体定義
//******************************************************************************************************************************
typedef enum
{
	UITEX_R = 0,			// 背景
	UITEX_L,				// 背景
	UI_KONMA,
	UITEX_MAX,			// 最大数
}UITEX;
typedef struct
{
	D3DXVECTOR3 pos;		// 位置
	D3DXCOLOR col;			// 色
	UITEX nType;			// 種類
	float fWidth;			// 幅
	float fHeight;			// 高さ
	float fWidthInit;			// 幅
	float fHeightInit;			// 高さ

	bool bUse;				// 使用されているかどうが
}UI;
//******************************************************************************************************************************
// プロトタイプ宣言
//******************************************************************************************************************************
void InitUI(void);
void UninitUI(void);
void UpdateUI(void);
void DrawUI(void);
void SetUI(D3DXVECTOR3 pos, D3DXCOLOR col, UITEX nType, float fWidth, float fHeight);
void DeleteUI(UITEX nType);
#endif
